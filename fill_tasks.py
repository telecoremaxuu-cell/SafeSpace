def fill_tasks_in_db():
    print("Создание таблиц в БД (если их нет)...")
    models.Base.metadata.create_all(bind=database.engine)
    
    db: Session = database.SessionLocal()
    try:
        task_count = db.query(models.Task).count()
        if task_count >= len(TASKS_DATA):
            print(f"✅ Таблица 'tasks' уже содержит {task_count} записей. Заполнение не требуется.")
            return

        print("Заполнение таблицы 'tasks'...")
        for day, text in TASKS_DATA.items():
            if not db.query(models.Task).filter(models.Task.day == day).first():
                db.add(models.Task(day=day, text=text))
        db.commit()
        print("✅ База данных успешно заполнена контентом 'Путь 21 дня'.")
    finally:
        db.close()

if __name__ == "__main__":
    fill_tasks_in_db()